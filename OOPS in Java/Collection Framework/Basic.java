

//What is Java Collection Framework?

  //      > Java Collection Framework is a set of classes and interfaces that help you store, manipulate, and process groups of objects like a collection (list, set, map, etc.).



//It is present in the java.util package.


  //      ---

    //     Why Use Collection Framework?

//Traditional Array	Collection Framework

//Fixed size (can't grow)	Dynamic size (auto grows/shrinks)
  //      Only stores same type	Can store different or same types
    //    No useful methods	Many built-in methods (add, remove, sort etc.)



//---

  //      Main Interfaces of Collection Framework:

//Collection (interface)
         //    |
         //            -------------------------
           //          |           |           |
//List        Set         Queue

//And separately:

//Map (not part of Collection, but in the framework)


//---

  //       Important Interfaces and Classes:

//Interface	Class (Implementation)	Description

  //      List	ArrayList, LinkedList	Ordered collection (allows duplicates)
      //  Set	HashSet, LinkedHashSet, TreeSet	Unordered, no duplicates
    //    Queue	PriorityQueue, ArrayDeque	Follows FIFO
        //Map	HashMap, TreeMap, LinkedHashMap	Key-value pairs



